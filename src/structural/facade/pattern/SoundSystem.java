package structural.facade.pattern;

/**
 * Represents a Sound System in the home theater.
 * Provides methods to turn the sound system on and off.
 */
public class SoundSystem {

    /**
     * Turns on the sound system.
     */
    public void on() {
        System.out.println("Sound system on!");
    }

    /**
     * Turns off the sound system.
     */
    public void off() {
        System.out.println("Sound system off!");
    }
}
